import numpy as num

arr=num.array([1,2,3,4])
print(arr)

#creating a 3x3 matrix of zero
zero=num.zeros((3,3))
print(zero)

#creating a matrix with specific range
one=num.ones((2,4))
print(one)

#range
range_array=num.arange(1,10,2)
print(range_array)

#linspace
linspace_array=num.linspace(0,1,5)
print(linspace_array)